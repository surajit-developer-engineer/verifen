    <script>
        $(document).ready(function () {
    var current_fs, next_fs, previous_fs;
    var opacity;
    var current = 1;
    var steps = $("fieldset").length;

    setProgressBar(current);
    updateProgressBar(0);

    function setProgressBar(curStep) {
        var percent = parseFloat(100 / steps) * curStep;
        $(".progress-bar").css("width", percent.toFixed() + "%");
    }

    function updateProgressBar(stepIndex) {
        $("#progressbar li").each(function (index) {
            $(this).removeClass("active completed");
            if (index < stepIndex) {
                $(this).addClass("completed");
            } else if (index === stepIndex) {
                $(this).addClass("active");
            }
        });
    }

    // Handle fieldset transitions
    $(".next").click(function () {
        current_fs = $(this).parent();
        next_fs = $(this).parent().next();

        next_fs.show();
        current_fs.animate({ opacity: 0 }, {
            step: function (now) {
                opacity = 1 - now;
                current_fs.css({ display: 'none', position: 'relative' });
                next_fs.css({ opacity: opacity });
            },
            duration: 500
        });

        var stepIndex = $("fieldset").index(next_fs);
        updateProgressBar(stepIndex);
        setProgressBar(++current);
    });

    $(".previous").click(function () {
        current_fs = $(this).parent();
        previous_fs = $(this).parent().prev();

        previous_fs.show();
        current_fs.animate({ opacity: 0 }, {
            step: function (now) {
                opacity = 1 - now;
                current_fs.css({ display: 'none', position: 'relative' });
                previous_fs.css({ opacity: opacity });
            },
            duration: 500
        });

        var stepIndex = $("fieldset").index(previous_fs);
        updateProgressBar(stepIndex);
        setProgressBar(--current);
    });

    // Handle form-card substeps
    $("fieldset").each(function () {
        var $fieldset = $(this);
        var $cards = $fieldset.find(".form-card");
        var $nextBtn = $fieldset.find(".next");
        var cardIndex = 0;

        function showCard(index) {
            $cards.removeClass("active").hide();
            var $activeCard = $cards.eq(index);
            $activeCard.data("completed", false); // Reset for each show
            $activeCard.addClass("active").fadeIn();
            $nextBtn.hide();

            // Setup triggers
            var $buttons = $activeCard.find(".button");
            var $inputs = $activeCard.find("input, select");

            if ($buttons.length > 0) {
                $buttons.off("click.substep").on("click.substep", function () {
                    $buttons.removeClass("selected");
                    $(this).addClass("selected");
                    goToNextCard();
                });
            }

            if ($inputs.length > 0) {
                $inputs.off(".substep");

                // Use `change` instead of `input` to avoid auto-skip
                $inputs.on("change.substep", function () {
                    var $this = $(this);
                    if ($this.val().trim() !== "") {
                        goToNextCard();
                    }
                });
            }

            // No input or button? Show next card
            if ($buttons.length === 0 && $inputs.length === 0) {
                goToNextCard();
            }
        }

        function goToNextCard() {
            if (cardIndex < $cards.length - 1) {
                cardIndex++;
                showCard(cardIndex);
            } else {
                $nextBtn.fadeIn();
            }
        }

        showCard(cardIndex);
    });
});
    </script>

